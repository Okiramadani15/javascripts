//contoh operasi unary
let result = +2;
document.writeln ( "<p>" + result + "</p>" )

result --;
document.writeln ( "<p>" + result + "</p>" )
//berati hasilnya adalah 2-1 = 1

result ++ ;
document.writeln ( "<p>" + result + "</p>" )
//berati hasilnya adalah 1+1 = 2

result = -5;
document.writeln ( "<p>" + result + "</p>" )
//berati merubah resoult jadi minus = -5

result = +6;
document.writeln ( "<p>" + result + "</p>" )
//berati merubah resoult jadi positif = 5
