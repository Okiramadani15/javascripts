//kita bisa mengirim parameter ke function yang ingin kita panggil
//untuk melakukan hal tersebut, kita perlu menambahkan parameter atau argument di function yang kita buat 
//parameter ditempatkan di dalam kurung () di deklarasikan method
//parameter bisa lebih dari satu, jika lebih dari satu harus dipisah menggunakan tanda koma


//membuat function dengan parameter
function sayHi (firstname, lastname) {
  document.writeln(`<p> hello ${firstname} ${lastname}</p>`);
}

//memanggil function dengan parameter
sayHi ("oki", "rama");
sayHi ("aan", "irol");


  
