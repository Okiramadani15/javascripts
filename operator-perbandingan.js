//contoh operasi perbandingan 
let result = 5 == "5";
document.writeln ( "<p> " + result + " </p>" )
 // hasil result jadi  = true
// dikarenakan nilai result adalah dinggap sama

result = 5 === "5";
document.writeln ( "<p> " + result + " </p>" ) 
 // hasil result jadi  = false
// dikarenakan nilai result adalah dinggap tidak sama
 
result = 5 > 10;
document.writeln ( "<p> " + result + " </p>" ) 
 // hasil result jadi  = false
// dikarenakan nilai result dari 5 tidak lebih besar dari 10 

result = 5 < 10;
document.writeln ( "<p> " + result + " </p>" ) 
 // hasil result jadi  = true
// dikarenakan nilai result dari 5 memang lebih kecil dari 10 

result = 5 <= 10;
document.writeln ( "<p> " + result + " </p>" ) 
 // hasil result jadi  = true
// dikarenakan nilai result dari 5 memang lebih  kecil dari 10 

result = 5 >= 10;
document.writeln ( "<p> " + result + " </p>" ) 
 // hasil result jadi  = false
// dikarenakan nilai result dari 5 tidak lebih besar dari 10



